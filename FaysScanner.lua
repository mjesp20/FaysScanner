-- Single addon merging FaysPlanner and RaidEnchantScanner

-- UI Frame creation for FaysPlanner output
local function CreateCopyFrame(text)
    if not FaysPlannerFrame then
        local f = CreateFrame("Frame", "FaysPlannerFrame", UIParent)
        f:SetWidth(400)
        f:SetHeight(300)
        f:SetPoint("CENTER", nil, "CENTER", 0, 0)
        f:SetBackdrop({
            bgFile = "Interface/DialogFrame/UI-DialogBox-Background",
            edgeFile = "Interface/DialogFrame/UI-DialogBox-Border",
            tile = true, tileSize = 32, edgeSize = 32,
            insets = { left = 8, right = 8, top = 8, bottom = 8 }
        })
        f:SetMovable(true)
        f:EnableMouse(true)
        f:RegisterForDrag("LeftButton")
        f:SetScript("OnDragStart", function(self) self:StartMoving() end)
        f:SetScript("OnDragStop", function(self) self:StopMovingOrSizing() end)

        local scrollFrame = CreateFrame("ScrollFrame", "FaysPlannerScrollFrame", f, "UIPanelScrollFrameTemplate")
        scrollFrame:SetPoint("TOPLEFT", f, "TOPLEFT", 12, -12)
        scrollFrame:SetPoint("BOTTOMRIGHT", f, "BOTTOMRIGHT", -30, 12)

        local editBox = CreateFrame("EditBox", "FaysPlannerEditBox", scrollFrame)
        editBox:SetMultiLine(true)
        editBox:SetFontObject(GameFontNormal)
        editBox:SetWidth(360)
        editBox:SetHeight(260)
        editBox:SetAutoFocus(true)
        editBox:SetPoint("TOPLEFT", scrollFrame, "TOPLEFT", 0, 0)
        editBox:SetScript("OnEscapePressed", function() f:Hide() end)

        scrollFrame:SetScrollChild(editBox)
        f.editBox = editBox

        local closeButton = CreateFrame("Button", nil, f, "UIPanelCloseButton")
        closeButton:SetPoint("TOPRIGHT", f, "TOPRIGHT", -5, -5)
        closeButton:SetScript("OnClick", function() f:Hide() end)

        f:Hide()
    end

    FaysPlannerFrame.editBox:SetText(text or "")
    FaysPlannerFrame.editBox:HighlightText()
    FaysPlannerFrame:Show()
end


-- Raid Info Generator
local function GenerateRaidInfo()
    local validClasses = {
        ["WARRIOR"] = true,
        ["PRIEST"] = true,
        ["MAGE"] = true,
        ["ROGUE"] = true,
        ["DRUID"] = true,
        ["HUNTER"] = true,
        ["WARLOCK"] = true,
        ["SHAMAN"] = true,
        ["PALADIN"] = true
    }

    local classSortedMembers = {}
    local numRaidMembers = GetNumRaidMembers()
    DEFAULT_CHAT_FRAME:AddMessage("[FaysScanner] Number of raid members: " .. numRaidMembers)

    for i = 1, numRaidMembers do
        local name, _, _, _, class = GetRaidRosterInfo(i)
        local normalizedClass = class and string.upper(class)
        if name and normalizedClass and validClasses[normalizedClass] then
            if not classSortedMembers[normalizedClass] then
                classSortedMembers[normalizedClass] = {}
            end
            table.insert(classSortedMembers[normalizedClass], name)
        end
    end

    local classOrder = {
        "WARRIOR", "PRIEST", "MAGE", "ROGUE", 
        "DRUID", "HUNTER", "WARLOCK", "SHAMAN", "PALADIN"
    }

    local output = ""
    for _, className in ipairs(classOrder) do
        if classSortedMembers[className] then
            table.sort(classSortedMembers[className])
            for _, name in ipairs(classSortedMembers[className]) do
                output = output .. name .. "," .. string.lower(className) .. "\n"
            end
        end
    end

    if output == "" then
        output = "No raid members found.\nTotal members = " .. numRaidMembers
    end

    if not SUPERWOW_VERSION then
        DEFAULT_CHAT_FRAME:AddMessage("[FaysScanner] No SuperWoW detected - showing output in UI")
        CreateCopyFrame(output)
    else
        ExportFile("raidinfo", output)
    end
end


-- Raid Enchant Scanner
local RaidEnchantScanner = {}

local EquipmentSlots = {
  Head = 1, Shoulder = 3, Chest = 5, Waist = 6, Legs = 7, Feet = 8,
  Wrist = 9, Hands = 10, Finger1 = 11, Finger2 = 12,
  Trinket1 = 13, Trinket2 = 14, Back = 15, MainHand = 16, OffHand = 17
}

local slotsOrder = {
  "Head", "Shoulder", "Chest", "Waist", "Legs", "Feet",
  "Wrist", "Hands", "Finger1", "Finger2", "Trinket1", "Trinket2",
  "Back", "MainHand", "OffHand"
}

local raidData = {}

local function SplitString(input, sep)
  local fields = {}
  local start = 1
  sep = sep or ":"

  while true do
    local sepStart, sepEnd = string.find(input, sep, start, true)
    if not sepStart then
      table.insert(fields, string.sub(input, start))
      break
    end
    table.insert(fields, string.sub(input, start, sepStart - 1))
    start = sepEnd + 1
  end

  return fields
end

local function GetEnchantFromLink(link)
    if not link then return "" end
    local fields = SplitString(link, ":")
    local enchantID = tonumber(fields[3] or "")
    if enchantID and enchantID > 0 then
        local enchantData = EnchantIDs and EnchantIDs[enchantID]
        if enchantData and enchantData.effect then
            return enchantData.effect
        else
            return "Unknown (" .. enchantID .. ")"
        end
    end
    return ""
end

function RaidEnchantScanner:Scan()
    raidData = {}
    for i = 1, GetNumRaidMembers() do
        local unit = "raid" .. i
        if UnitExists(unit) then
            local name = UnitName(unit)
            raidData[name] = {}

            for slotName, slotID in pairs(EquipmentSlots) do
                local itemLink = GetInventoryItemLink(unit, slotID)
                local enchant = GetEnchantFromLink(itemLink)
                raidData[name][slotName] = enchant
            end
        end
    end

    RaidEnchantScanner:Export()
end

function RaidEnchantScanner:Export()
    local lines = {}
    local header = "Name," .. table.concat(slotsOrder, ",")
    table.insert(lines, header)

    for name, slots in pairs(raidData) do
        local row = { name }
        for _, slot in ipairs(slotsOrder) do
            table.insert(row, slots[slot] or "")
        end
        table.insert(lines, table.concat(row, ","))
    end

    local output = table.concat(lines, "\n")

    if not SUPERWOW_VERSION then
        DEFAULT_CHAT_FRAME:AddMessage("[FaysScanner] No SuperWoW detected - showing output in UI")
        CreateCopyFrame(output)
    else
        ExportFile("RaidEnchants.csv", output)
        DEFAULT_CHAT_FRAME:AddMessage("[FaysScanner] Exported enchant info to RaidEnchants.csv.txt")
    end
end



